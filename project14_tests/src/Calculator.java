public class Calculator {       // Класс для арифметических операций

    public int sum(int a, int b){
        return a + b;
    }

    public int sub(int a, int b){
        return a - b;
    }

    public int mul(int a, int b){
        return a*b;
    }

    public double div(int a, int b){
        return (double)a/(double)b;
    }


}
