public class Human {
    private int age;
    private String name;
    private char sex;
    private String fatherName;

    public Human(int age, String name, char sex) {
        this.age = age;
        this.name = name;
        this.sex = sex;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public char getSex() {
        return sex;
    }

    public String getFatherName() {
        return fatherName;
    }



}

